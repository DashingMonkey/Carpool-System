package entity;

public class Road {
	private String road_id;
	private String road_name;
	private String road_id_pre;
	private String road_id_next;

	public String getRoad_id() {
		return road_id;
	}

	public void setRoad_id(String road_id) {
		this.road_id = road_id;
	}

	public String getRoad_name() {
		return road_name;
	}

	public void setRoad_name(String road_name) {
		this.road_name = road_name;
	}

	public String getRoad_id_pre() {
		return road_id_pre;
	}

	public void setRoad_id_pre(String road_id_pre) {
		this.road_id_pre = road_id_pre;
	}

	public String getRoad_id_next() {
		return road_id_next;
	}

	public void setRoad_id_next(String road_id_next) {
		this.road_id_next = road_id_next;
	}

}
