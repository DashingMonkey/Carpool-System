package entity;

public class Tutor {
	private int id;
	private String name;
	private String pwd;
	private String major;
	private String info;
	private String graduate1;
	private String graduate2;
	private String graduate3;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getGraduate1() {
		return graduate1;
	}

	public void setGraduate1(String graduate1) {
		this.graduate1 = graduate1;
	}

	public String getGraduate2() {
		return graduate2;
	}

	public void setGraduate2(String graduate2) {
		this.graduate2 = graduate2;
	}

	public String getGraduate3() {
		return graduate3;
	}

	public void setGraduate3(String graduate3) {
		this.graduate3 = graduate3;
	}

}
